class employee():
	def __init__(self, fname, lname, dayPref, hourPref, id):
		self.fname = fname
		self.lname = lname
		self.dayPref = dayPref
		self.hourPref = hourPref
		self.id = id
		
	def getFullName(self):
		return self.fname + " " + self.lname
		
	def getfname(self):
		return fname
		
	def getlname(self):
		return lname
	
	def getDayPref(self):
		return dayPref
	
	def getHourPref(self):
		return hourPref
		
	def getId(self):
		return id
		